#!/usr/bin/perl

# Scalars start with a $ sign followed by a letter

my $name = "Shajan";
my $age = 39.1;

print "Hello ", $name, " You are ", $age, "\n";

# Naming Convention, $local_vars, $Global_Variables $CONSTANT

my $num = 3.5e19;

print "Num : ", $num;

# Arithmetic

my $x = 100;
my $y = 2;

print $x * $y, "\n";

# Exponential operator **

print $y ** 8, "\n";

# print

print "Hello $name\n";
